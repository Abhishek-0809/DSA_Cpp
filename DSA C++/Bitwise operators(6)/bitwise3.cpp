// INCREMENT, DECREMENT
#include <iostream>
using namespace std;
int main()
{

    int i = 7;

    cout << (i) << endl;
    // i=7

    cout << (i++) << endl;
    // print 7, i=8

    cout << (++i) << endl;
    // print 9, i=9

    cout << (i--) << endl;
    // print 9, i=8

    cout << (--i) << endl;
    // print 7, i=7
}